    public void mostrar(){
       
        String sql = "select * from lista_carreras2";
        try {
            cn = mysql.conectar();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            
            Object[] carreras = new Object[3];
            DefaultTableModel modelo = (DefaultTableModel) tabla_carreras.getModel();
            
            modelo = (DefaultTableModel) tabla_carreras.getModel();
            while (rs.next()) {
                carreras[0] = rs.getInt("carr_id");
                carreras[1] = rs.getString("carr_nombre");
                carreras[2] = rs.getString("carr_descripcion");
               
                modelo.addRow(carreras);
            }
            tabla_carreras.setModel(modelo);
            cn.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error MostrarCarreras " + e);
        }
    }
