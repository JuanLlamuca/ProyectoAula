    public void llenarTabla() {
        String sql = "select * from lista_carreras2;";
        try {
            cn = mysql.conectar();
            st = cn.createStatement();
            rs = st.executeQuery(sql);

            Object[] carrera = new Object[3];
            DefaultTableModel modelo = (DefaultTableModel) tabla_carreras.getModel();

            modelo = (DefaultTableModel) tabla_carreras.getModel();
            while (rs.next()) {
                carrera[0] = rs.getInt("carr_id");
                carrera[1] = rs.getString("carr_nombre");
                carrera[2] = rs.getString("carr_descripcion");
                modelo.addRow(carrera);
            }
            tabla_carreras.setModel(modelo);
            cn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error ModifcarCarreras: " + e);
        }
    }

