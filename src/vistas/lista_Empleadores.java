     public void mostrar() {

        String sql = "select * from lista_empleador";
        try {
            cn = mysql.conectar();
            st = cn.createStatement();
            rs = st.executeQuery(sql);

            Object[] empleador = new Object[10];
            DefaultTableModel modelo = (DefaultTableModel) tabla_empleadores.getModel();

            modelo = (DefaultTableModel) tabla_empleadores.getModel();
            while (rs.next()) {
                empleador[0] = rs.getInt("emp_cedula");
                empleador[1] = rs.getString("emp_nombres");
                empleador[2] = rs.getString("emp_apellidos");
                empleador[3] = rs.getString("emp_correo");
                empleador[4] = rs.getString("emp_nomEmpresa");
                empleador[5] = rs.getString("emp_laboCum");
                empleador[6] = rs.getString("emp_ubiEmpresa");
                empleador[7] = rs.getInt("emp_telEmpresa");
                empleador[8] = rs.getString("emp_correoEmpresa");
                empleador[9] = rs.getString("emp_desEmpresa");

                modelo.addRow(empleador);
            }
            tabla_empleadores.setModel(modelo);
            cn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error MostrarCarreras " + e);
        }
    }

